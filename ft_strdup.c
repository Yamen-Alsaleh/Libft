#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*ptr;

	if (s == NULL)
	{
		ptr = NULL;
		return (ptr);
	}
	i = 0;
	while (s[i])
		i++;
	ptr = (char *)malloc(i + 1);
	if (!ptr)
		return (NULL);
	i = 0;
	while (s[i])
	{
		ptr[i] = s[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
